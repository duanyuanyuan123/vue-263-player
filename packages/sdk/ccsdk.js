    sendMessage(key, content) {
      return new Promise((resolve, reject) => {
        this.promisePool[key] = {
          content,
          resolve,
          reject
        };
        this.socket.send(`cmd:${key}\r\nid:${uuid(32, 16)}\r\n\r\n${JSON.stringify(content)}`);
      });
    }
